//! No-panic baseline + checker (PR #151 of the v0.5.0 ladder).
//!
//! Two commands:
//!
//! - `cargo xtask no-panic baseline [--reset]` — show diff between source
//!   and the on-disk baseline (`policy/no-panic-baseline.toml`); with
//!   `--reset`, regenerate the baseline from a fresh source scan
//!   (single-purpose dedicated PRs only).
//! - `cargo xtask check-no-panic-family` — CI gate that fails when source
//!   contains panic-family findings exceeding the baseline counts.
//!
//! Mode is `no-new-debt`: existing baselined findings are allowed; new
//! occurrences fail the check. See [`docs/NO_PANIC_POLICY.md`](../../docs/NO_PANIC_POLICY.md).
//!
//! Source scanning is regex-based (single-line patterns only) and skips
//! test scopes by path: `tests/**`, `fuzz/**`, `crates/shiplog-testkit/**`.
//! Lines starting with `//` (line comments) are also skipped to avoid
//! false-positives in doc examples.

use anyhow::{Context, Result, bail};
use regex::Regex;
use serde::{Deserialize, Serialize};
use std::collections::BTreeMap;
use std::fs;
use std::path::Path;
use std::process::Command;
use std::sync::OnceLock;

use crate::policy;
use crate::tasks::file_policy::Mode;

// ────────────────────────────────────────────────────────────────────────
// Identity + Baseline
// ────────────────────────────────────────────────────────────────────────

#[derive(Debug, Clone, Eq, PartialEq, Hash, Ord, PartialOrd)]
struct Identity {
    path: String,
    family: String,
    selector_kind: String,
    selector_callee: String,
    snippet: String,
}

#[derive(Debug, Clone, Deserialize, Serialize)]
struct BaselineEntry {
    path: String,
    family: String,
    selector_kind: String,
    selector_callee: String,
    snippet: String,
    count: usize,
}

impl BaselineEntry {
    fn identity(&self) -> Identity {
        Identity {
            path: self.path.clone(),
            family: self.family.clone(),
            selector_kind: self.selector_kind.clone(),
            selector_callee: self.selector_callee.clone(),
            snippet: self.snippet.clone(),
        }
    }
}

#[derive(Debug, Deserialize, Serialize)]
struct BaselineFile {
    schema_version: u32,
    policy: String,
    owner: String,
    status: String,
    #[serde(default)]
    policy_config: Option<PolicyConfig>,
    #[serde(default, rename = "entry")]
    entries: Vec<BaselineEntry>,
}

#[derive(Debug, Deserialize, Serialize)]
struct PolicyConfig {
    mode: String,
}

fn baseline_path(workspace_root: &Path) -> std::path::PathBuf {
    policy::policy_dir(workspace_root).join("no-panic-baseline.toml")
}

fn read_baseline(path: &Path) -> Result<BaselineFile> {
    let text =
        fs::read_to_string(path).with_context(|| format!("read baseline {}", path.display()))?;
    toml::from_str(&text).with_context(|| format!("parse baseline {}", path.display()))
}

fn read_baseline_counts(path: &Path) -> BTreeMap<Identity, usize> {
    match read_baseline(path) {
        Ok(file) => file
            .entries
            .into_iter()
            .map(|e| (e.identity(), e.count))
            .collect(),
        Err(_) => BTreeMap::new(),
    }
}

fn write_baseline(path: &Path, counts: &BTreeMap<Identity, usize>) -> Result<()> {
    let mut entries: Vec<BaselineEntry> = counts
        .iter()
        .map(|(id, count)| BaselineEntry {
            path: id.path.clone(),
            family: id.family.clone(),
            selector_kind: id.selector_kind.clone(),
            selector_callee: id.selector_callee.clone(),
            snippet: id.snippet.clone(),
            count: *count,
        })
        .collect();
    entries.sort_by(|a, b| {
        a.path
            .cmp(&b.path)
            .then(a.family.cmp(&b.family))
            .then(a.selector_callee.cmp(&b.selector_callee))
            .then(a.snippet.cmp(&b.snippet))
    });
    let file = BaselineFile {
        schema_version: 1,
        policy: "no-panic-baseline".to_string(),
        owner: "EffortlessMetrics".to_string(),
        status: "advisory".to_string(),
        policy_config: Some(PolicyConfig {
            mode: "no-new-debt".to_string(),
        }),
        entries,
    };
    let header = "# Generated by `cargo xtask no-panic baseline --reset`. Do not hand-edit.\n# See docs/NO_PANIC_POLICY.md.\n\n";
    let body = toml::to_string_pretty(&file).context("serialize baseline")?;
    fs::write(path, header.to_string() + &body)
        .with_context(|| format!("write {}", path.display()))?;
    Ok(())
}

// ────────────────────────────────────────────────────────────────────────
// Source scanning
// ────────────────────────────────────────────────────────────────────────

#[derive(Debug, Clone)]
struct PatternSpec {
    family: &'static str,
    selector_kind: &'static str,
    selector_callee: &'static str,
}

fn unwrap_re() -> &'static Regex {
    static RE: OnceLock<Regex> = OnceLock::new();
    RE.get_or_init(|| Regex::new(r"\.unwrap\s*\(\s*\)").expect("static regex"))
}

fn expect_re() -> &'static Regex {
    static RE: OnceLock<Regex> = OnceLock::new();
    RE.get_or_init(|| Regex::new(r#"\.expect\s*\(\s*"[^"]*"\s*\)"#).expect("static regex"))
}

fn macro_re(name: &'static str) -> Regex {
    Regex::new(&format!(r"\b{name}\s*!\s*\(")).expect("static regex")
}

fn scan_workspace(workspace_root: &Path) -> Result<BTreeMap<Identity, usize>> {
    let files = git_ls_rs_files(workspace_root)?;
    let mut counts: BTreeMap<Identity, usize> = BTreeMap::new();
    for file in &files {
        if is_test_scope(file) {
            continue;
        }
        let path = workspace_root.join(file);
        let Ok(text) = fs::read_to_string(&path) else {
            continue;
        };
        for line in text.lines() {
            let trimmed_for_skip = line.trim_start();
            if trimmed_for_skip.starts_with("//") {
                continue;
            }
            scan_line(file, line, &mut counts);
        }
    }
    Ok(counts)
}

fn scan_line(file: &str, line: &str, counts: &mut BTreeMap<Identity, usize>) {
    let snippet = line.trim().to_string();
    if unwrap_re().is_match(line) {
        bump(
            counts,
            file,
            PatternSpec {
                family: "unwrap",
                selector_kind: "method-call",
                selector_callee: "Option/Result::unwrap",
            },
            &snippet,
        );
    }
    if expect_re().is_match(line) {
        bump(
            counts,
            file,
            PatternSpec {
                family: "expect",
                selector_kind: "method-call",
                selector_callee: "Option/Result::expect",
            },
            &snippet,
        );
    }
    for (name, family) in [
        ("panic", "panic"),
        ("unreachable", "unreachable"),
        ("todo", "todo"),
        ("unimplemented", "unimplemented"),
    ] {
        if macro_re(name).is_match(line) {
            bump(
                counts,
                file,
                PatternSpec {
                    family,
                    selector_kind: "macro-call",
                    selector_callee: name,
                },
                &snippet,
            );
        }
    }
}

fn bump(counts: &mut BTreeMap<Identity, usize>, file: &str, spec: PatternSpec, snippet: &str) {
    let id = Identity {
        path: file.replace('\\', "/"),
        family: spec.family.to_string(),
        selector_kind: spec.selector_kind.to_string(),
        selector_callee: spec.selector_callee.to_string(),
        snippet: snippet.to_string(),
    };
    *counts.entry(id).or_insert(0) += 1;
}

fn is_test_scope(path: &str) -> bool {
    let p = path.replace('\\', "/");
    p.starts_with("tests/")
        || p.contains("/tests/")
        || p.starts_with("fuzz/")
        || p.starts_with("crates/shiplog-testkit/")
        || p.starts_with("xtask/")
        || p == "build.rs"
        || p.ends_with("/build.rs")
}

fn git_ls_rs_files(workspace_root: &Path) -> Result<Vec<String>> {
    let output = Command::new("git")
        .args(["ls-files", "*.rs"])
        .current_dir(workspace_root)
        .output()
        .context("invoke git ls-files *.rs")?;
    if !output.status.success() {
        bail!(
            "git ls-files *.rs failed: {}",
            String::from_utf8_lossy(&output.stderr)
        );
    }
    Ok(String::from_utf8(output.stdout)
        .context("git output not valid UTF-8")?
        .lines()
        .filter(|l| !l.is_empty())
        .map(String::from)
        .collect())
}

